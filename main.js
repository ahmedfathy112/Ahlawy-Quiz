let currentQuestion = 0;
let questions = document.querySelectorAll(".question");
let correctAnswersCount = 0; // متغير يحمل عدد الأسئلة الصحيحة

function checkAnswer(answer, button) {
  // تحديد ما إذا كانت الإجابة صحيحة وزيادة عدد الإجابات الصحيحة إذا كانت الإجابة صحيحة
  if (answer === "رامي ربيعه" && button.textContent.includes("رامي ربيعه")) {
    button.classList.add("correct");
    alert("اجابه صح يبن اللاعيبه😉😉");
    correctAnswersCount++;
  } else if (answer === "22") {
    // button.classList.add("correct");
    alert("اجابه صح يبن اللاعيبه😉😉");
    correctAnswersCount++;
  } else if (answer === "ابوتريكه" && button.textContent.includes("ابوتريكه")) {
    button.classList.add("correct");
    alert("اجابه صح يبن اللاعيبه😉😉");
    correctAnswersCount++;
  } else if (
    answer === "بيرسي تاو" &&
    button.textContent.includes("بيرسي تاو")
  ) {
    button.classList.add("correct");
    alert("اجابه صح يبن اللاعيبه😉😉");
    correctAnswersCount++;
  } else if (
    answer === "حسام غالي" &&
    button.textContent.includes("حسام غالي")
  ) {
    button.classList.add("correct");
    alert("اجابه صح يبن اللاعيبه😉😉");
    correctAnswersCount++;
  } else {
    button.classList.add("wrong");
    alert("للاسف اجابتك غلط...حد يغلط في دي بردو!!");
  }
  // تحديث نص العنصر الذي يحمل الفئة .score بقيمة عدد الإجابات الصحيحة
  document.getElementById("scoreValue").textContent = correctAnswersCount;
  // تعطيل جميع الأزرار بعد الإجابة
  document
    .querySelectorAll(".question")
    [currentQuestion].querySelectorAll(".answer")
    .forEach(function (btn) {
      btn.disabled = true;
    });
}

function moveToNextQuestion() {
  if (currentQuestion >= questions.length) {
    alert("Quiz is finished!");
    return;
  }
  if (
    currentQuestion > 0 &&
    !questions[currentQuestion - 1].querySelector("button.correct")
  ) {
    alert("You have not answered the previous question!");
    return;
  }
  questions[currentQuestion].style.display = "none";
  currentQuestion++;
  if (currentQuestion < questions.length) {
    questions[currentQuestion].style.display = "block";
  }
}

// Show the first question initially
questions[currentQuestion].style.display = "block";
